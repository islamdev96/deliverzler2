    BuildContext context,
    String text, {
    Key? key,
    Color? color,
    FontWeight? weight,
    bool? underline,
    TextAlign? textAlign,
    EdgeInsetsGeometry? margin,
    EdgeInsetsGeometry? padding,
    double? height,
    int? maxLines,
    Alignment? alignment,
  }) : this._(
          context,
          text,
          key: key,
          maxLines: maxLines,
          size: Sizes.font32(context),
          padding: padding,
          margin: margin,
          height: height,
          color: color ?? customColors(context).font32Color,
          weight: weight ?? FontStyles.fontWeightBlack,
          textAlign: textAlign,
          alignment: alignment,
        );

