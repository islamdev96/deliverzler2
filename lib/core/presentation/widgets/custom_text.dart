class CustomText extends Container {
  CustomText._(
    BuildContext context,
    String text, {
    Key? key,
    double? size,
    color,
    weight,
    width,
    bool underline = false,
    strutStyle,
    textAlign,
    textDirection,
    locale,
    softWrap,
    overflow,
    textScaleFactor,
    maxLines,
    semanticsLabel,
    textWidthBasis,
    height,
    textHeightBehavior,
    EdgeInsetsGeometry? margin,
    EdgeInsetsGeometry? padding,
    Alignment? alignment,
  }) : super(
            key: key,
            margin: margin,
            padding: padding,
            alignment: alignment ?? AlignmentDirectional.topStart,
            width: width,
            child: Text(
              text,
              style: TextStyle(
                color: color,
                height: height,
                fontSize: size,
                fontWeight: weight,
                decoration: underline ? TextDecoration.underline : null,
                fontFamily: FontStyles.fontFamily(context),
              ),
              strutStyle: strutStyle,
              textAlign: textAlign,
              textDirection: textDirection,
              locale: locale,
              softWrap: softWrap,
              overflow: overflow,
              textScaleFactor: textScaleFactor,
              maxLines: maxLines,
              semanticsLabel: semanticsLabel,
              textWidthBasis: textWidthBasis,
              textHeightBehavior: textHeightBehavior,
            ));

